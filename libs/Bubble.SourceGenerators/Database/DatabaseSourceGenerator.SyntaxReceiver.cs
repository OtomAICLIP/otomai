using System.Diagnostics.CodeAnalysis;
using Bubble.SourceGenerators.Database.Models;
using Bubble.SourceGenerators.Infrastructure.Extensions;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;

namespace Bubble.SourceGenerators.Database;

public sealed partial class DatabaseSourceGenerator
{
    private static bool IsValidSymbol([NotNullWhen(true)] INamedTypeSymbol? symbol)
    {
        return symbol is not null &&
               !symbol.Name.Equals("Object") &&
               !symbol.Name.Equals("EntityRecord");
    }

    private static bool Predicate(SyntaxNode syntaxNode, CancellationToken cancellationToken)
    {
        cancellationToken.ThrowIfCancellationRequested();

        if (syntaxNode is not ClassDeclarationSyntax declarationSyntax)
            return false;

        if (declarationSyntax.AttributeLists.Count is 0)
            return false;

        return declarationSyntax
            .AttributeLists
            .SelectMany(x => x.Attributes)
            .Any(x => x.Name.ToString() is "Table");
    }

    private static DbTable Transform(GeneratorSyntaxContext context, CancellationToken cancellationToken)
    {
        cancellationToken.ThrowIfCancellationRequested();

        if (context.SemanticModel.GetDeclaredSymbol(context.Node) is not INamedTypeSymbol originalSymbol)
            throw new Exception("Symbol not found.");

        var tableAttribute = originalSymbol
            .GetAttributes()
            .First(x => x.AttributeClass?.Name is "TableAttribute");

        var tableName = tableAttribute.ConstructorArguments[0].Value?.ToString() ?? throw new Exception("Table name not found.");

        var tableType = (DbTypes)tableAttribute.ConstructorArguments[1].Value!;

        var dbColumns = new List<DbColumn>();

        var i = 0;

        var sequence = default(string);

        for (var symbol = originalSymbol; IsValidSymbol(symbol); symbol = symbol.BaseType)
        {
            var properties = symbol
                .GetMembers()
                .OfType<IPropertySymbol>()
                .Where(x => x is { Kind: SymbolKind.Property, DeclaredAccessibility: Accessibility.Public, IsStatic: false } && !x.Name.StartsWith("get_") &&
                            !x.Name.StartsWith("set_"))
                .ToArray();

            foreach (var property in properties)
            {
                var attributes = property.GetAttributes();

                var computedAttribute = attributes.FirstOrDefault(x => x.AttributeClass?.Name is "ComputedAttribute");

                if (computedAttribute is not null)
                    continue;

                var fullType = property.Type.ToDisplayString();

                if (fullType.Contains('.'))
                    fullType = fullType.Split('.').Last();

                if (fullType.EndsWith("?"))
                    fullType = fullType.Substring(0, fullType.Length - 1);

                var autoGeneratedAttribute = attributes.FirstOrDefault(x => x.AttributeClass?.Name is "AutoGeneratedAttribute");

                sequence = autoGeneratedAttribute
                    ?
                    .ConstructorArguments
                    .FirstOrDefault()
                    .Value?
                    .ToString();

                i++;

                dbColumns.Add(new DbColumn
                {
                    Name = property.Name,
                    DbName = property.Name.ToSnakeCase(),
                    VariableName = property.Name.ToCamelCase(),
                    FullType = fullType,
                    Index = i,
                    IsValueType = property.Type.IsValueType,
                    IsRequired = property.IsRequired,
                    IsInitOnly = property.SetMethod?.IsInitOnly ?? false,
                    IsReadOnly = property.SetMethod?.IsReadOnly ?? false,
                    IsAutoGenerated = autoGeneratedAttribute is not null,
                    IsNullable = property.NullableAnnotation is NullableAnnotation.Annotated ||
                                 property.Type.Name is "Nullable" ||
                                 property.Type.ToDisplayString().EndsWith("?")
                });
            }
        }

        return new DbTable
        {
            Namespace = originalSymbol.ContainingNamespace.ToDisplayString(),
            Name = originalSymbol.Name,
            IdentifiableName = originalSymbol.Name.Replace("Record", string.Empty),
            DbName = tableName,
            DbType = tableType,
            SequenceName = sequence,
            IsSealed = originalSymbol.IsSealed,
            Columns = dbColumns.DistinctBy((x, y) => x.Name.Equals(y.Name)).OrderBy(x => x.Index)
        };
    }
}